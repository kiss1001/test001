/**
 * 单例模板
 */
export default class Singleton
{
    private static singleInstance = null;

    public static getInstance<T>(c: { new(): T;}): T
    {
        if (this.singleInstance == null)
        {
            this.singleInstance = new c();
        }
        return this.singleInstance;
    }
}
